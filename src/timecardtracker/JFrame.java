    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String inputUsername = tfLoginUsername.getText();
        String inputPassword = tfLoginPassword.getText();
        String sql =
            "Select employeeID, username, password, supervisor " +
            "From employees " +
            "Where username = '" + inputUsername + "'" +
            "And password = '" + inputPassword + "'";
            
        try (Connection conn = getConnection();
            PreparedStatement ps = conn.prepareStatement(sql)) {
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                int empID = rs.getInt("employeeID");
                String toBool = String.valueOf(rs.getBoolean("supervisor"));
                
                if (toBool == "true") {
                    jPanel3.show();
                    jPanel1.hide();
                    rs.close();
                } else if (toBool == "false") {
                    jPanel5.show();
                    jPanel1.hide();
                    rs.close();
                } else {
                    JOptionPane.showMessageDialog(null, "something went wrong.");
                }
                String x = Integer.toString(empID);
                tfCurrentUserID.setText(x);
            } else {
                tfLoginPassword.setText("");
                tfLoginUsername.requestFocus();
                JOptionPane.showMessageDialog(null, "Username or Password incorrect");
            }
        }catch (SQLException e) {
            tfLoginPassword.setText("");
            tfLoginUsername.requestFocus();
            JOptionPane.showMessageDialog(null, "No connection to database");
        }
    }//GEN-LAST:event_loginButtonActionPerformed

