    private void btnFindProjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindProjectActionPerformed
        String id = tfProjectID.getText();
        String sql =
            "Select projectID, startDate, description, endDate, employeeID " +
            "From projects " +
            "Where projectID = ?";
        try (Connection conn = getConnection();
            PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, id);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                int userID = rs.getInt("employeeID");
                if (userID == Integer.valueOf(tfCurrentUserID.getText())) {
                    String projectID = rs.getString("projectID");
                    tfProjectID.setText(projectID);
                    String desc = rs.getString("description");
                    tfProjectDescription.setText(desc);
                    String sDate = rs.getString("startDate");
                    lblStartDate.setText(sDate);
                    String endDate = rs.getString("endDate");
                    lblEndDate.setText(endDate);
                    btnStartProj.setVisible(false);
                    btnEndProj.setVisible(false);
                    tfProjectDescription.setEditable(true);
                    lblStartDate.show();
                    lblEndDate.show();
                    startLabel.setVisible(true);
                    endLabel.setVisible(true);
                    JOptionPane.showMessageDialog(null, "Success!");
                    rs.close();
                } else {
                    JOptionPane.showMessageDialog(null, "You must be the owner to access this project!");
                    rs.close();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Could not find that project!");
                rs.close();
            }
            
        }catch (SQLException e) {
            tfLoginPassword.setText("");
            tfLoginUsername.requestFocus();
            JOptionPane.showMessageDialog(null, "No connection to database");
        }
    }//GEN-LAST:event_btnFindProjectActionPerformed

